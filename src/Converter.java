public class Converter {

    public double metersToMilimeters(double meters){
        return meters * 1000;
    }
    public double metersToCentimeters(double meters){
        return meters * 100;
    }
    public double millimetersToMeters(double millimeters){
        return millimeters / 1000;
    }
    public double millimetersToCentimeters(double millimeters){
        return millimeters /10;
    }
    public double centimetersToMeters (double centimeters){
        return centimeters/100;
    }
    public double centimetersToMilimeters (double centimeters){
        return centimeters * 10;
    }

    public double kilogramsToGrams (double kilogram){
        return kilogram * 1000;
    }
    public double kilogramsToMilligrams (double kilogram){
        return kilogram * 1000000;
    }
    public double gramsToKilograms (double grams){
        return grams / 1000;
    }
    public double gramsToMilligrams (double grams){
        return grams * 1000;
    }
    public double milligramsToKilograms (double milligrams){
        return milligrams / 1000000;
    }
    public double milligramsToGrams (double milligrams){
        return milligrams / 1000;
    }


}
